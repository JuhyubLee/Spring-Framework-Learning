package com.kg.util;

public class Calculator {
	
	// Calculator 클래스의 sub 메서드에 대한 테스트를 진행하고
	// 거기에 대한 로그를 원하는 형태로 남겨보세요.
	
	// 테스트 : sub 클래스로 연산을 10번 진행
	// 로그 : 계산에 사용된 값 2개와 결과를 로그로 남기기
	
	public int sum(int left, int right) {
		return left + right;
	}
	
	public int sub(int left, int right) {
		return left - right;
	}
	
	

}


